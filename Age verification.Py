def check_age():
    try:
        # Запрашиваем возраст у пользователя
        age = input("Введите ваш возраст: ")
        
        # Преобразуем в целое число
        age = int(age)
        
        # Проверяем, что возраст положительный
        if age < 0:
            print("Ошибка: Возраст не может быть отрицательным!")
        elif age >= 18:
            print("Вы совершеннолетний(я).")
        else:
            print("Вы несовершеннолетний(я).")
            
    except ValueError:
        # Обрабатываем случай, когда введено не число
        print("Ошибка: Введите целое число!")

# Запускаем функцию
check_age()